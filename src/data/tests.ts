// Helper to build 25 questions with 1-based index scores
function buildShumkinQuestions(texts: string[], optionsSet: [string, string, string, string][]): Question[] {
  return texts.map((text, i) => {
    const qOpts = optionsSet[i];
    return {
      id: i + 1,
      text,
      options: [
        { text: qOpts[0], score: 1 },
        { text: qOpts[1], score: 2 },
        { text: qOpts[2], score: 3 },
        { text: qOpts[3], score: 4 }
      ]
    };
  });
}

